inline void init(){
    num[1] = 1;
    num[2] = 13;
    for(int i = 3; i <= 18257; i++){
        num[i] = num[i - 1] + (i * 2 + (i - 2) * 2 + 1) * 2 + (2 * i - 3) * 2;
    }
}

