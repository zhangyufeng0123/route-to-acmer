#include<bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int num[100];

int main(void) {
#ifdef ACM_LOCAL
    freopen("in.txt", "r", stdin);
    freopen("out.txt", "w", stdout);
#endif

    ios::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    ll n;
    cin >> n;
    int k = 0;
    while (n) {
        num[k++] = n % 2;
        n /= 2;
    }
    ll ans = 0, ret = 1;
    for (int i = 0; i < 16; i++) {
        swap(num[i], num[i + 16]);
    }
    for (int i = 0; i < 32; i++) {
        ans += num[i] * ret;
        ret <<= 1;
    }
    cout << ans << endl;
}