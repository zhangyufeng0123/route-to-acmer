#include<bits/stdc++.h>

using namespace std;

typedef long long ll;

const int maxn = (int)1e6 + 100;
ll num[12][maxn];

void init(){
    for(int i = 2; i <= 10; i++){
        num[i][1] = 1;
        for(int j = 2; j <= 1000000; j++){
            int ret = j;
            ll ans = 0;
            while(ret){
                ans += ret % i;
                ret /= i;
            }
            num[i][j] = num[i][j - 1] + ans;
        }
    }
}

int main(void) {
#ifdef ACM_LOCAL
    freopen("in.txt", "r", stdin);
    freopen("out.txt", "w", stdout);
#endif
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int T, cas = 1;
    cin >> T;
    init();
    while(T--){
        int n, b;
        cin >> n >> b;
        cout << "Case #" << cas++ << ": ";
        cout << num[b][n] << endl;
    }
}